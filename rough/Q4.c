// print the no 0 to 10 using for loop ?
#include <stdio.h>
int main()
{
    for (int i = 0; i <= 10; i++)
    {
        printf("%d", i);
    }

    // print 1 to 10 float value using for loop.

    for (float i = 1; i <= 10; i++)
    {
        printf("%f", i);
    }

    // print a to z charachters using for loop.

    for (char ch = 'A'; ch <= 'Z'; ch++)
    {
        printf("%c", ch);
    }

    return 0;
}
